function generateProceduralTitles(targetCount: number): typeof TOP_GAMES {
    const generated: typeof TOP_GAMES = [];

    const adjectives = ['Dark', 'Super', 'Cyber', 'Neon', 'Cosmic', 'Shadow', 'Final', 'Infinite', 'Crystal', 'Iron', 'Steel', 'Magic', 'Epic', 'Lost', 'Ancient', 'Silent', 'Mega', 'Hyper', 'Ultra', 'Secret', 'Eternal', 'Crimson', 'Azure', 'Golden', 'Silver', 'Savage', 'Wild', 'Rogue', 'Prime', 'Elite', 'Alpha', 'Omega', 'Phantom', 'Ghost', 'Divine', 'Fallen', 'Rising', 'Brave', 'Glorious'];
    const nouns = ['Quest', 'Warrior', 'Empire', 'Legends', 'Chronicles', 'Saga', 'Horizon', 'Odyssey', 'Blade', 'Souls', 'Hearts', 'Fantasy', 'World', 'Land', 'City', 'Force', 'Squad', 'Racers', 'Fighters', 'Heroes', 'Knights', 'Dragons', 'Space', 'Simulator', 'Tycoon', 'Manager', 'Survival', 'Craft', 'Battle', 'War', 'Agent', 'Mission', 'Protocol', 'Destiny', 'Legacy', 'Origins', 'League', 'Storm'];
    const platforms = ['PC', 'PlayStation 5', 'Xbox Series X', 'Nintendo Switch', 'Multi-platform', 'VR', 'Mobile'];
    const subtitles = ['Origins', 'Remastered', 'Returns', 'Forever', 'Online', 'Reborn', 'Evolution', 'GotY Edition', 'Deluxe', 'Ultimate', 'Director\'s Cut', 'Anniversary Edition', 'Collection', 'Trilogy'];

    // Generate specific sequels for volume
    const seriesWithSequels = [
        { base: 'Call of Duty', count: 20, startYear: 2003, platform: 'Multi-platform' },
        { base: 'Battlefield', count: 12, startYear: 2002, platform: 'Multi-platform' },
        { base: 'FIFA', count: 25, startYear: 1998, platform: 'Multi-platform' },
        { base: 'Madden NFL', count: 25, startYear: 1998, platform: 'Multi-platform' },
        { base: 'NBA 2K', count: 24, startYear: 2000, platform: 'Multi-platform' },
        { base: 'Final Fantasy', count: 16, startYear: 1987, platform: 'Multi-platform' },
        { base: 'Assassin\'s Creed', count: 13, startYear: 2007, platform: 'Multi-platform' },
        { base: 'Resident Evil', count: 12, startYear: 1996, platform: 'Multi-platform' },
        { base: 'The Sims', count: 4, startYear: 2000, platform: 'PC' },
        { base: 'Civilization', count: 6, startYear: 1991, platform: 'PC' },
        { base: 'Street Fighter', count: 6, startYear: 1987, platform: 'Multi-platform' },
        { base: 'Tekken', count: 8, startYear: 1994, platform: 'Multi-platform' },
        { base: 'Mortal Kombat', count: 12, startYear: 1992, platform: 'Multi-platform' },
        { base: 'Need for Speed', count: 20, startYear: 1994, platform: 'Multi-platform' },
        { base: 'Sonic the Hedgehog', count: 20, startYear: 1991, platform: 'Multi-platform' },
        { base: 'Tomb Raider', count: 12, startYear: 1996, platform: 'Multi-platform' },
        { base: 'Just Dance', count: 15, startYear: 2009, platform: 'Multi-platform' },
    ];

    // 1. Add Sequels
    for (const series of seriesWithSequels) {
        for (let i = 1; i <= series.count; i++) {
            generated.push({
                title: `${series.base} ${i > 1 ? i : ''}`.trim(),
                year: series.startYear + i - 1,
                platform: series.platform,
            });
        }
    }

    // 2. Generate Randomized Titles
    let attempts = 0;
    while (generated.length < targetCount && attempts < targetCount * 2) {
        const adj = adjectives[Math.floor(Math.random() * adjectives.length)];
        const noun = nouns[Math.floor(Math.random() * nouns.length)];
        const platform = platforms[Math.floor(Math.random() * platforms.length)];
        // Skew years towards recent
        const year = 1995 + Math.floor(Math.random() * 30); // 1995-2024

        let title = `${adj} ${noun}`;

        // 35% chance of sequel number
        if (Math.random() > 0.65) {
            title += ` ${Math.floor(Math.random() * 7) + 2}`;
        }
        // 25% chance of subtitle
        else if (Math.random() > 0.75) {
            const sub = subtitles[Math.floor(Math.random() * subtitles.length)];
            title += `: ${sub}`;
        }

        generated.push({ title, year, platform });
        attempts++;
    }

    return generated;
}

export function getFullGameList() {
    const manual = TOP_GAMES;
    // We want 10,000 total. Ensure we don't return fewer if manual is large.
    const needed = Math.max(0, 10000 - manual.length);
    const procedural = generateProceduralTitles(needed);
    return [...manual, ...procedural];
}
